int	ft_atoi(const char *str)
{
	int	i;
	int	k;
	int	s;

	i = 0;
	k = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == '\t')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = (k * 10) + (str[i] - 48);
		i++;
	}
	return (k * s);
}
